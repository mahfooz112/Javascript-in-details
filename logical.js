//Logical operator used to combine boolean value
 //(true or false)

 //AND = &&
 //OR = ||
 //NOT = !
    

    //AND operator
 const temp = 0;
 if(temp > 0 && temp <= 30){
  console.log("The weather is Good");
 }
 else{
  console.log("The Weather is Bad");
 }

    // ||operator
const temmmp = -250;
if(temmmp <= 0 || temmmp> 30){
  console.log("The Weather is Bad");
}
else{
  console.log("The Weather is Good");
}

//! Not operator

const isSunny = true;
if(!isSunny){
  console.log("It is Sunny");
}
else{
  console.log("It is Cloudy");
}






